#Escape Sequences
tabby_cat = "\tI'm tabbed in." #\t is tab space
persian_cat = "I'm split\non a line." #\ between line will split line
backslash_cat = "I'm \\ a \\ cat." #\\ escape single \

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print (tabby_cat)
print (persian_cat)
print (backslash_cat)
print (fat_cat)

